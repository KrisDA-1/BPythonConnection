lenguajes=["Python", "Ruby", "PHP", "JavaScript", "Java"] ##paréntesis cuadrados para listas
print(lenguajes [1]) ##imprime el segundo elemento de la lista, porque empieza en 0
lenguajes[1]="Go" ##cambiar el segundo elemento de la lista
print(lenguajes [-1]) ##recuperar el último elemento de la lista, -2 penúltimo, -3 antepenúltimo, etc.
print(lenguajes[:2]) ##imprime los dos primeros elementos de la lista
print(lenguajes) ##imprime toda la lista
print(lenguajes[2:]) ##imprime desde el tercer elemento hasta el último
print(lenguajes[1:3]) ##imprime desde el segundo elemento hasta el cuarto (sin incluirlo)